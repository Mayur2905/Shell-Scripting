# How to write comments in shell script

#!/bin/bash

# This is a comment
echo "Hello this is first shell script" # This is also a inline comment

# Strong quotes
echo ' my \
name \
is \
Mayur\
'

# Double quotes
echo "  my \
name \
is \
Mayur\
" # This is a multi-line comment

# tab in line
echo -e "Hello\tWorld"

# New line in line
echo -e "Hello\nWorld"

# Backspace in line
echo -e "Hello\bWorld"